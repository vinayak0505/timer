// Start building your app here!
@Composable
fun MyApp() {
    val nums = remember { mutableStateOf(10L) }
    val text = remember { mutableStateOf("count down") }
    val countNum = object : CountDownTimer(
        10000,
        1000
    ) {
        override fun onTick(millisUntilFinished: Long) {
            nums.value = millisUntilFinished / 1000
            text.value = "time remaining ${nums.value}"
        }

        override fun onFinish() {
            text.value = "time up"
        }
    }
    Surface(color = MaterialTheme.colors.background) {

        Column(
            modifier = Modifier.padding(16.dp)
        ) {
            Text(
                text = "${text.value}",
                modifier = Modifier.padding(16.dp)
            )
            Button(onClick = { countNum.start() }) {
                Text(text = "Start countdown numbers")
            }
        }
    }
}

